#include "libft.h"

int	ft_isalnum(char c)
{
	if (c == '\0')
		return (0);
	if ((c < 65 || c > 90) && (c < 97 || c > 122))
	{
		if (c < 48 || c > 57)
			return (0);
	}
	return (1);
}
