#include "ft_printf.h"

int	ft_print_str(char *s)
{
	int		i;
	char	c;

	i = 0;
	c = 0;
	while (s[i] != '\0')
	{
		write (1, &s[i], 1);
		i++;
	}
	return (i);
}
