#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	i;
	int	num;
	int	count;

	i = 0;
	num = 0;
	count = 1;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == 32)
		i++;
	if (nptr[i] && (nptr[i] == 43 || nptr[i] == 45))
	{
		if (nptr[i] == 45)
			count *= -1;
		i++;
	}
	while (nptr[i] && (nptr[i] >= '0' && nptr[i] <= '9'))
	{
		num = num * 10 + (nptr[i] - '0');
		i++;
	}
	return (num * count);
}
