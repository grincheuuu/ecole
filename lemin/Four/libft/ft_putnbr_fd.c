#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int long	nbr;
	int			i;

	nbr = n;
	if (nbr < 0)
	{
		write(fd, "-", 1);
		nbr = -nbr;
	}
	if (nbr >= 10)
	{
		ft_putnbr_fd(nbr / 10, fd);
		i = nbr % 10 + '0';
		write(fd, &i, 1);
	}
	if (nbr < 10)
	{
		i = nbr % 10 + '0';
		write(fd, &i, 1);
	}
}
